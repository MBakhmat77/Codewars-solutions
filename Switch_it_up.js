function switchItUp(number){
  let n;
switch (number){
    case 0: n = 'Zero';
    break;
case 1: n = 'One';
 break;  
case 2: n = 'Two';
break; 
case 3: n = 'Three';
break; 
case 4: n = 'Four';
break; 
case 5: n = 'Five';
break; 
case 6: n = 'Six';
break; 
case 7: n = 'Seven';
break; 
case 8: n ='Eight';
break;
case 9: n = 'Nine';
break;
}
return n;
}
